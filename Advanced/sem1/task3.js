// Часто при работе с DOM мы сталкиваемся с коллекциями 
// элементов, которые не являются стандартными
// массивами, но похожи на них.Однако у таких коллекций 
// нет методов массива, и здесь на помощь приходит
// Array.from.В этом задании вы научитесь конвертировать 
// коллекции DOM - элементов в массивы и работать
// с ними.
// Напишите функцию, которая собирает все элементы <div> 
// на странице, преобразует их в массив и
// фильтрует только те из них, у которых есть атрибут data-active.
// Выведите результат на консоль.

function filterActiveDivs() {
    const divs = document.querySelectorAll('div'); // Выбираем все элементы <div>
    const divsArray = Array.from(divs); // Преобразуем NodeList в массив
    const activeDivs = divsArray.filter(div => div.getAttribute('data-active') !== null); // Фильтруем только те элементы, у которых есть атрибут data-active
    return activeDivs;
}

// Вызываем функцию и выводим результат на консоль
console.log(filterActiveDivs());